#include	"delay.h"

//========================================================================
// 函数: void delay_ms(unsigned char ms)
// 描述: 延时函数。
// 参数: ms,要延时的ms数, 这里只支持1~255ms. 自动适应主时钟.
// 返回: none.
// 版本: VER1.0
// 日期: 2021-3-9
// 备注: 
//========================================================================
void delay_ms(unsigned int ms)
{
	unsigned int i;
	do
	{
		i = MAIN_Fosc / 10000;
		while(--i);
	}while(--ms);
}


void delay_us(unsigned int us)
{
	unsigned int i;
	do
	{
		i = MAIN_Fosc / 10000000;
		while(--i);
	}while(--us);
}

void delay_10us(u16 us)
{
unsigned int i;
	do
	{
		i = MAIN_Fosc / 1000000;
		while(--i);
	}while(--us);	
}

